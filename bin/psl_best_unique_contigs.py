def lines_from(a_filename):
    # it gives chunks
    fin = open(a_filename,'r')
    while True:
        lines = fin.readlines(10**8)
        if not lines:
            break
        gc.disable()
        lines = [line.rstrip('\r\n').split('\t') for line in lines]
        gc.enable()
        for line in lines:
            if line:
                yield line
    fin.close()

#########################
def lines_none_from(a_filename):
    # it gives chunks
    fin = open(a_filename,'r')
    while True:
        lines = fin.readlines(10**8)
        if not lines:
            break
        gc.disable()
        lines = [line.rstrip('\r\n').split('\t') for line in lines]
        gc.enable()
        for line in lines:
            if line:
                yield line
    yield None
    fin.close()

